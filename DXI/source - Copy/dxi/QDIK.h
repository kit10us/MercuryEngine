	inline const std::string DIKey( int iKey )
	{
		return g_szDIK[ iKey ];
	}

	inline int DIKey( const std::string & sKey )
	{
		for( int iKey = 0; g_szDIK[ iKey ] == 0; iKey++ )
			if( DIKey( iKey ) == sKey ) return iKey; 
		return -1;
	}
